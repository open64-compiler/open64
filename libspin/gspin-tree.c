gs_t
__gs (gs_code_t code)
{
  GS_ASSERT (code != DOT, "__gs() called with DOT");
  int size = gspin_node_size(code);
  gs_t node = (gs_t) __gs_mempool_alloc(GS_ARENA, size);
  _gs_code(node, code);
  if (gs_mempool[GS_ARENA].current_index >= 0x3fffffff) {
    fprintf(stderr, "gspin mempool overflow.\n");
    abort();
  }
#ifdef Is_True_On
  num_gspin_nodes++;
  arity_dist[tree_code_table[code].arity]++;
#endif
  return node;
}

#ifdef Is_True_On
void dump_arity_dist(void) {
  int i;
  for (i = 0; i < 45; i++)
    if (arity_dist[i] != 0)
      printf("%d: %d\n", i, arity_dist[i]);
}
#endif

gs_void_t
_gs_s (gs_t node, const gs_string_t s, gs_unsigned_long_t length)
{
  GS_ASSERT (node != (gs_t) NULL, "Got null node");
  node->u1.s = (gs_string_t) __gs_mempool_alloc (IB_STRING_ARENA, length);
  memcpy (gs_s(node), s, length);
  _gs_slen (node, length);
  return;
}

